#include <stdio.h>

int main() {
    int M;
    int S;
    int A = 0;
    int tax = 0;

    scanf("%d", &M);
    S = M - 3500;

    if (S > 0) {
        if (S <= 1500) {
            tax = S * 3 / 100;
        } else if (S <= 4500) {
            tax = 1500 * 3 / 100 + (S - 1500) * 10 / 100;
        } else if (S <= 9000) {
            tax = 1500 * 3 / 100 + 3000 * 10 / 100 + (S - 4500) * 20 / 100;
        } else if (S <= 35000) {
            tax = 1500 * 3 / 100 + 3000 * 10 / 100 + 4500 * 20 / 100 + (S - 9000) * 25 / 100;
        } else {
            tax = 1500 * 3 / 100 + 3000 * 10 / 100 + 4500 * 20 / 100 + 26000 * 25 / 100 + (S - 35000) * 30 / 100;
        }
    }

    A = M - tax;
    printf("%d", A);

    return 0;
}
