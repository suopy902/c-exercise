#include <stdio.h>
#include <stdlib.h>

int main()
{
    int M;
    int A;
    scanf("%d",&M);
    int S=M-3500;
    if(S<=1500){
        A=M-S*3/100;
    }
    else if(S<=4500){
        A=M-(1500*3/100+(S-1500)*10/100);
    }
    else if(S<=9000){
        A=M-(1500*3/100+3000*10/100+(S-4500)*20/100);
    }
    else if(S<=35000){
        A=M-(1500*3/100+3000*10/100+4500*20/100+(S-9000)*25/100);
    }
    else {
        A=M-(1500*3/100+3000*10/100+4500*20/100+26000*25/100+(S-35000)*30/100);
    }
    printf("%d",A);
    return 0;
}
